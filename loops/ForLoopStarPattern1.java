package loops;

public class ForLoopStarPattern1 {
	
	public static void main(String[] args) {
		//*****
		//*****
		//*****
		//*****
		//*****
		// we just simply put in other for loop.
		// then after complete first loop we systemn.out.println blank statement.
		
		for (int i =1; i<=5; i++) {
			
			for (int j =1; j<=5; j++)
			{
				System.out.print("*");
				
			}
			System.out.println();
		}
		
	}
	

}

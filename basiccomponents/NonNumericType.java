package basiccomponents;

public class NonNumericType {
	
	public static void main(String[] args) {
		
		// with the help of char Data Type we Print(Any Character / Symbol / Number ).
		// when we used char we give ''(single inverted comma)
		//syntax = char veriableName = 'Value';
		//in char we used only one character it may be any.
		// size of char=2 byte
		char c ='c';
		System.out.println(c);
		char c1 = '@';
		System.out.println(c1);
		char c2 ='1';
		System.out.println(c2);
		
		
		//in boolean Data Type we give only (True/False) value other values not accept boolean.
		// size of boolean = N.A
		boolean b = true;
		boolean b1 = false;
		System.out.println(b);
		System.out.println(b1);
	}

}

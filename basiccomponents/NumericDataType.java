package basiccomponents;

public class NumericDataType {
	
	public static void main(String[] args) {
		
//		byte  = 1 byte
//		short = 2 byte
//		int   = 4 byte
//		long  = 8 byte
		
		
		byte b = 10;
		byte b1 = 11;
		
		System.out.println(b+b1);

		short s =2;
		short s1= 5;

		System.out.println(s1);
		
		int i = 10;
		int i1 = 10;
		
		System.out.println(i);
		
		// when we used long Data Type then we have to give (l) small letter l
		long l = 10l;
		long l1 = 551l;
		long l2 =l+l1;
		System.out.println(l2);
		
	}

}
